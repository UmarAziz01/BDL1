    public void loadDataTransaksi() {
        tableModel.setRowCount(0); // Bersihkan tabel
        List<Transaksi> transaksiList = crud.tampilTransaksi(); //list<nama class>
        Collections.sort(transaksiList, Comparator.comparingInt(Transaksi::getId_transaksi));
        tableModel.setColumnIdentifiers(new Object[]{"Tanggal", "ID Transaksi", "ID Pelanggan", "ID Produk", "Jumlah", "Total Harga"});

        for (Transaksi t : transaksiList) { //for(nama class...
            tableModel.addRow(new Object[]{t.getTanggal(), t.getId_transaksi(), t.getId_pelanggan(), t.getId_produk(), t.getJumlah(), t.getHarga()});
        }

    }

