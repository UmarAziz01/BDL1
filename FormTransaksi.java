    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        try {
            String tgl = tfTanggal.getText();
            int idTr = Integer.parseInt(tfIDTransaksi.getText());
            int idPe = Integer.parseInt(tfIDPelanggan.getText());
            int idPr = Integer.parseInt(tfIDProduk.getText());
            int jml = Integer.parseInt(tfJumlah.getText());
            Double tHarga = Double.valueOf(tfTotalHarga.getText());

            if (crud.tambahTransaksi(tgl, idTr, idPe, idPr, jml, tHarga)) {
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan.");
                loadDataTransaksi();
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menambahkan data.");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Input ID dan Umur harus berupa angka.");
        }
    }//GEN-LAST:event_btnTambahActionPerformed

